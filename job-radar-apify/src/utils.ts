export function htmlEntities(str: string): string {
  if (!str) return '';
  return str
    .replaceAll('&amp;', '&')
    .replaceAll('&lt;', '<')
    .replaceAll('&gt;', '>')
    .replaceAll('&quot;', '"')
    .replaceAll('&#39;', "'")
    .replaceAll('&apos;', "'")
    .replaceAll('&#225;', 'á')
    .replaceAll('&#233;', 'é')
    .replaceAll('&#237;', 'í')
    .replaceAll('&#243;', 'ó')
    .replaceAll('&#250;', 'ú')
    .replaceAll('&#241;', 'ñ')
    .replaceAll('&#193;', 'Á')
    .replaceAll('&#201;', 'É')
    .replaceAll('&#205;', 'Í')
    .replaceAll('&#211;', 'Ó')
    .replaceAll('&#218;', 'Ú')
    .replaceAll('&#209;', 'Ñ')
    .replaceAll('&#252;', 'ü')
    .replaceAll('&#220;', 'Ü')
    .replaceAll('&#231;', 'ç')
    .replaceAll('&#199;', 'Ç')
    .replaceAll('&#x2F;', '/')
    .replaceAll('&#xF1;', 'ñ')
    .replaceAll('&#xE1;', 'á')
    .replaceAll('&#xE9;', 'é')
    .replaceAll('&#xED;', 'í')
    .replaceAll('&#xF3;', 'ó')
    .replaceAll('&#xFA;', 'ú')
    .replaceAll('&#xC1;', 'Á')
    .replaceAll('&#xC9;', 'É')
    .replaceAll('&#xCD;', 'Í')
    .replaceAll('&#xD3;', 'Ó')
    .replaceAll('&#xDA;', 'Ú')
    .replaceAll('&#xD1;', 'Ñ');
}
